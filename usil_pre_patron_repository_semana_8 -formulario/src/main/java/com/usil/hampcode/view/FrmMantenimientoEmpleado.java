    private void inicializarControlador() {
        String fuenteDatos = rbMySQL.isSelected() ? "mysql" : "archivo";
        empleadoController = new EmpleadoController(fuenteDatos);
    }

    private void cargarEmpleadosTabla() {
        List<Empleado> empleados = empleadoController.obtenerTodosLosEmpleados();

        String[] columnas = {"ID", "Nombre", "Edad", "Departamento"};
        Object[][] datos = new Object[empleados.size()][4];

        for (int i = 0; i < empleados.size(); i++) {
            Empleado empleado = empleados.get(i);
            datos[i][0] = empleado.getId();
            datos[i][1] = empleado.getNombre();
            datos[i][2] = empleado.getEdad();
            datos[i][3] = empleado.getDepartamento();
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(datos, columnas));
    }

    private void llenarComboBoxDepartamento() {
        cboDepartamento.removeAllItems();
        String[] departamentos = {"Recursos Humanos", "IT", "Marketing", "Ventas", "Finanzas"};
        for (String departamento : departamentos) {
            cboDepartamento.addItem(departamento);
        }
    }

    private void mostrarMensaje(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje);
    }

    private void seleccionarFuenteDatos(String fuente) {
        if (fuente.equals("mysql")) {
            rbArchivo.setSelected(false);
        } else {
            rbMySQL.setSelected(false);
        }

        inicializarControlador();
        cargarEmpleadosTabla();
    }

