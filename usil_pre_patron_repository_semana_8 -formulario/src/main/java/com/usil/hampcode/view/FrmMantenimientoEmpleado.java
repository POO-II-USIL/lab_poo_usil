    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        inicializarControlador();

        String nombre = txtNombre.getText();
        int edad;
        try {
            edad = Integer.parseInt(txtEdad.getText());
        } catch (NumberFormatException e) {
            mostrarMensaje("Edad no v치lida.");
            return;
        }
        String departamento = cboDepartamento.getSelectedItem().toString();

        Empleado empleado = new Empleado(0, nombre, edad, departamento);
        String mensaje = empleadoController.agregarEmpleado(empleado);
        mostrarMensaje(mensaje);
        cargarEmpleadosTabla();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        inicializarControlador();
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
            String nombre = txtNombre.getText();
            int edad;
            try {
                edad = Integer.parseInt(txtEdad.getText());
            } catch (NumberFormatException e) {
                mostrarMensaje("Edad no v치lida.");
                return;
            }
            String departamento = cboDepartamento.getSelectedItem().toString();

            Empleado empleado = new Empleado(id, nombre, edad, departamento);
            String mensaje = empleadoController.actualizarEmpleado(empleado);
            mostrarMensaje(mensaje);
            cargarEmpleadosTabla();
        } else {
            mostrarMensaje("Por favor, selecciona un empleado para modificar.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        inicializarControlador();
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
            String mensaje = empleadoController.eliminarEmpleado(id);
            mostrarMensaje(mensaje);
            cargarEmpleadosTabla();
        } else {
            mostrarMensaje("Por favor, selecciona un empleado para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

