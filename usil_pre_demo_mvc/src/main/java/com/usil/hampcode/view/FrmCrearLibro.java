    // Método para limpiar el formulario
    private void limpiarFormulario() {
        txtIsbn.setText("");
        txtTitulo.setText("");
        txtSinopsis.setText("");
        cboAutor.setSelectedIndex(0);
        rbTerror.setSelected(true);  // Seleccionar el primer género por defecto
        ckLeido1.setSelected(false);
        ckLNoLeido.setSelected(true);
    }

    // Método para obtener el género seleccionado
    private String getGeneroSeleccionado() {
        if (rbTerror.isSelected()) {
            return "Terror";
        } else if (rbRomance.isSelected()) {
            return "Romance";
        } else if (rbCienciaFiccion1.isSelected()) {
            return "Ciencia Ficción";
        } else if (rbFantasia.isSelected()) {
            return "Fantasía";
        }
        return "Desconocido";
    }
    
    // Método para cargar los autores en el JComboBox utilizando AutorController
    private void cargarAutoresEnComboBox() {
        try {
            List<Autor> autores = autorController.listarAutores();  // Obtener autores desde AutorController
            DefaultComboBoxModel<String> modelo = new DefaultComboBoxModel<>();
            for (Autor autor : autores) {
                modelo.addElement(autor.getNombre());  // Agregar el nombre del autor al modelo del ComboBox
            }
            cboAutor.setModel(modelo);  // Establecer el modelo en el JComboBox
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar autores.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    // Método para mostrar un diálogo para ingresar un nuevo autor
    private void mostrarDialogoNuevoAutor() {
        String nuevoAutor = JOptionPane.showInputDialog(this, "Ingrese el nombre del nuevo autor:", "Nuevo Autor", JOptionPane.PLAIN_MESSAGE);

        if (nuevoAutor != null && !nuevoAutor.trim().isEmpty()) {
            try {
                // Crear el objeto Autor y guardarlo usando AutorController
                Autor autor = new Autor(nuevoAutor);
                autorController.guardarAutor(autor);

                // Volver a cargar los autores en el JComboBox
                cargarAutoresEnComboBox();
                JOptionPane.showMessageDialog(this, "Autor guardado exitosamente.");
            } catch (IOException e) {               
                JOptionPane.showMessageDialog(this, "Error al guardar el autor.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de autor válido.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }
    
