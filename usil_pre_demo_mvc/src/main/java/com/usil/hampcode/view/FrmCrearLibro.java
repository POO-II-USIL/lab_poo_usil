    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        
         try {
            // Capturar los datos del formulario
            String isbn = txtIsbn.getText();
            String titulo = txtTitulo.getText();
            Autor autorSeleccionado = new Autor(cboAutor.getSelectedItem().toString());
            String genero = getGeneroSeleccionado();
            String sinopsis = txtSinopsis.getText();
            boolean leido = ckLeido1.isSelected();
            boolean loTengo = !ckLNoLeido.isSelected();  // Al revés de "No Leído"

            // Crear un objeto Libro con los datos capturados
            Libro libro = new Libro(isbn, titulo, genero, sinopsis,leido, loTengo,autorSeleccionado);

            // Llamar al método guardarLibro del controlador para guardarlo
            libroController.guardarLibro(libro);

            // Mostrar mensaje de éxito
            JOptionPane.showMessageDialog(this, "Libro guardado exitosamente.");

            // Limpiar el formulario después de guardar
            limpiarFormulario();

        } catch (IOException e) {           
            JOptionPane.showMessageDialog(this, "Error al guardar el libro.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

